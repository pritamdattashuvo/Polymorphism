
public class Calculator {

	public int add(int x,int y) {
		int z = x+y;
		
		return z;
	}
	
	public double add(int x,double y) {
		double z = x+y;
		
		return z;
	}
	
	public int add(int x,int y,int z) {
		int w = x+y+z;
		
		return w;
	}
	public double add(double x,double y,double z) {
		double w = x+y+z;
		
		return w;
	}
}
